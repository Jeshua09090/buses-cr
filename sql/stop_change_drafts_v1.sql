create or replace function public._shift_ruta_paradas_for_insert(
  p_ruta_id bigint,
  p_sentido text,
  p_target_order integer
)
returns void
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_target integer := greatest(0, coalesce(p_target_order, 0));
begin
  update public.ruta_paradas
  set orden = orden + 1000000
  where ruta_id = p_ruta_id
    and sentido = p_sentido
    and orden >= v_target;

  update public.ruta_paradas
  set orden = orden - 999999
  where ruta_id = p_ruta_id
    and sentido = p_sentido
    and orden >= v_target + 1000000;
end;
$$;

create or replace function public._shift_ruta_paradas_for_delete(
  p_ruta_id bigint,
  p_sentido text,
  p_deleted_order integer
)
returns void
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_deleted integer := greatest(0, coalesce(p_deleted_order, 0));
begin
  update public.ruta_paradas
  set orden = orden + 1000000
  where ruta_id = p_ruta_id
    and sentido = p_sentido
    and orden > v_deleted;

  update public.ruta_paradas
  set orden = orden - 1000001
  where ruta_id = p_ruta_id
    and sentido = p_sentido
    and orden > v_deleted + 1000000;
end;
$$;

