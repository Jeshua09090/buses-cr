create or replace function public.sugerir_insercion_parada_en_ruta(
  p_ruta_id integer,
  p_lat double precision,
  p_lng double precision,
  p_sentido text default 'ambos'
)
returns table (
  ruta_id integer,
  sentido text,
  suggested_order integer,
  before_orden integer,
  before_parada_id bigint,
  before_parada_nombre text,
  after_orden integer,
  after_parada_id bigint,
  after_parada_nombre text,
  total_stops integer,
  distancia_trazado_m integer,
  distancia_prev_m integer,
  distancia_next_m integer,
  progreso_fraccion numeric
)
language sql
stable
set search_path = public, extensions
as $$
with route_line as (
  select
    rp.ruta_id,
    st_makeline(
      st_setsrid(st_makepoint(rp.lng, rp.lat), 4326)
      order by coalesce(rp.segmento_id, 0), coalesce(rp.orden, 0)
    ) as geom
  from public.ruta_puntos rp
  where rp.ruta_id = p_ruta_id
    and rp.lat is not null
    and rp.lng is not null
  group by rp.ruta_id
  having count(*) >= 2
),
requested_senses as (
  select sense as sentido
  from (
    select distinct rp.sentido as sense
    from public.ruta_paradas rp
    where rp.ruta_id = p_ruta_id
      and (
        case
          when coalesce(p_sentido, 'ambos') in ('ida', 'vuelta', 'loop')
            then rp.sentido = p_sentido
          else rp.sentido in ('ida', 'vuelta', 'loop')
        end
      )
    union all
    select
      case
        when coalesce(p_sentido, 'ambos') in ('ida', 'vuelta', 'loop') then p_sentido
        else 'ida'
      end
    where not exists (
      select 1
      from public.ruta_paradas rp
      where rp.ruta_id = p_ruta_id
        and (
          case
            when coalesce(p_sentido, 'ambos') in ('ida', 'vuelta', 'loop')
              then rp.sentido = p_sentido
            else rp.sentido in ('ida', 'vuelta', 'loop')
          end
        )
    )
  ) senses
),
target as (
  select
    rl.ruta_id,
    rs.sentido,
    st_setsrid(st_makepoint(p_lng, p_lat), 4326)::geography as point_geo,
    rl.geom,
    st_distance(
      st_setsrid(st_makepoint(p_lng, p_lat), 4326)::geography,
      rl.geom::geography
    )::integer as distancia_trazado_m,
    st_linelocatepoint(
      rl.geom,
      st_setsrid(st_makepoint(p_lng, p_lat), 4326)
    ) as point_fraction
  from route_line rl
  cross join requested_senses rs
),
route_stops as (
  select
    t.ruta_id,
    t.sentido,
    rp.orden,
    rp.parada_id,
    coalesce(p.nombre, 'Parada de buses') as parada_nombre,
    p.geo,
    st_linelocatepoint(t.geom, p.geo::geometry) as stop_fraction
  from target t
  join public.ruta_paradas rp
    on rp.ruta_id = t.ruta_id
   and rp.sentido = t.sentido
  join public.paradas p
    on p.id = rp.parada_id
),
prev_stop as (
  select distinct on (rs.sentido)
    rs.sentido,
    rs.orden,
    rs.parada_id,
    rs.parada_nombre,
    st_distance(t.point_geo, rs.geo)::integer as distancia_stop_m
  from target t
  join route_stops rs
    on rs.sentido = t.sentido
   and rs.stop_fraction <= t.point_fraction
  order by rs.sentido, rs.stop_fraction desc, rs.orden desc
),
next_stop as (
  select distinct on (rs.sentido)
    rs.sentido,
    rs.orden,
    rs.parada_id,
    rs.parada_nombre,
    st_distance(t.point_geo, rs.geo)::integer as distancia_stop_m
  from target t
  join route_stops rs
    on rs.sentido = t.sentido
   and rs.stop_fraction >= t.point_fraction
  order by rs.sentido, rs.stop_fraction asc, rs.orden asc
),
stop_totals as (
  select
    rs.sentido,
    count(*)::integer as total_stops
  from route_stops rs
  group by rs.sentido
)
select
  t.ruta_id,
  t.sentido,
  case
    when ns.orden is not null then ns.orden
    when ps.orden is not null then ps.orden + 1
    else 0
  end as suggested_order,
  ps.orden as before_orden,
  ps.parada_id as before_parada_id,
  ps.parada_nombre as before_parada_nombre,
  ns.orden as after_orden,
  ns.parada_id as after_parada_id,
  ns.parada_nombre as after_parada_nombre,
  coalesce(st.total_stops, 0) as total_stops,
  t.distancia_trazado_m,
  ps.distancia_stop_m as distancia_prev_m,
  ns.distancia_stop_m as distancia_next_m,
  round((t.point_fraction::numeric), 6) as progreso_fraccion
from target t
left join prev_stop ps
  on ps.sentido = t.sentido
left join next_stop ns
  on ns.sentido = t.sentido
left join stop_totals st
  on st.sentido = t.sentido
order by
  case t.sentido
    when 'ida' then 1
    when 'vuelta' then 2
    when 'loop' then 3
    else 4
  end;
$$;

