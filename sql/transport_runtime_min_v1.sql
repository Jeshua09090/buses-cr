create or replace function public.paradas_por_ruta(p_ruta_id integer, radio_metros integer default 50)
returns table(parada_id integer, nombre text, lat numeric, lng numeric)
language sql
as $$
  select distinct on (p.id)
    p.id, p.nombre, p.lat, p.lng
  from public.paradas p
  join public.ruta_puntos rp
    on st_dwithin(coalesce(p.geog, p.geo), rp.geog, radio_metros)
  where rp.ruta_id = p_ruta_id
  order by p.id;
$$;

create or replace function public.buscar_rutas_cercanas(
  lat_origen double precision,
  lng_origen double precision,
  lat_destino double precision,
  lng_destino double precision,
  radio_metros integer default 500
)
returns table(
  ruta_id integer,
  operador text,
  nombre_ruta text,
  codigo_ctp text,
  dist_origen double precision,
  dist_destino double precision
)
language sql
stable
as $$
  with
  pto as (select st_setsrid(st_makepoint(lng_origen, lat_origen), 4326)::geography as g),
  ptd as (select st_setsrid(st_makepoint(lng_destino, lat_destino), 4326)::geography as g),
  cerca_origen as (
    select rp.ruta_id, min(st_distance(rp.geog, pto.g)) as dist_origen
    from public.ruta_puntos rp, pto
    where st_dwithin(rp.geog, pto.g, radio_metros)
    group by rp.ruta_id
  ),
  cerca_destino as (
    select rp.ruta_id, min(st_distance(rp.geog, ptd.g)) as dist_destino
    from public.ruta_puntos rp, ptd
    where st_dwithin(rp.geog, ptd.g, radio_metros)
    group by rp.ruta_id
  )
  select
    r.id,
    r.operador,
    r.nombre_ruta,
    r.codigo_ctp,
    co.dist_origen,
    cd.dist_destino
  from public.rutas r
  join cerca_origen co on r.id = co.ruta_id
  join cerca_destino cd on r.id = cd.ruta_id
  order by (co.dist_origen + cd.dist_destino)
  limit 15;
$$;

grant execute on function public.paradas_por_ruta(integer, integer) to anon, authenticated;
grant execute on function public.buscar_rutas_cercanas(double precision, double precision, double precision, double precision, integer) to anon, authenticated;
