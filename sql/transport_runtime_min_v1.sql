create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

create table if not exists public.paradas (
  id integer generated by default as identity primary key,
  osm_id bigint,
  lat numeric not null,
  lng numeric not null,
  nombre text,
  operador text,
  rutas_red text,
  tiene_techo boolean not null default false,
  tiene_banca boolean default false,
  iluminada boolean default false,
  accesible boolean not null default false,
  geog geography(Point, 4326),
  creado_en timestamptz default now(),
  activo boolean not null default true,
  fuente text not null default 'manual',
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  geo geography(Point, 4326) generated always as (
    st_setsrid(st_makepoint((lng)::double precision, (lat)::double precision), 4326)::geography
  ) stored
);

create unique index if not exists paradas_osm_id_uidx
  on public.paradas (osm_id)
  where osm_id is not null;

create index if not exists paradas_geo_gix
  on public.paradas using gist (geo);

create index if not exists paradas_activas_idx
  on public.paradas (activo)
  where activo = true;

create table if not exists public.ruta_paradas (
  id bigint generated by default as identity primary key,
  ruta_id bigint not null references public.rutas(id) on delete cascade,
  parada_id bigint not null references public.paradas(id) on delete cascade,
  sentido text not null default 'ida',
  orden integer not null,
  es_subida boolean not null default true,
  es_bajada boolean not null default true,
  distancia_acumulada_m integer,
  tiempo_estimado_desde_inicio_min integer,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint ruta_paradas_orden_unique unique (ruta_id, sentido, orden),
  constraint ruta_paradas_sentido_chk check (sentido in ('ida', 'vuelta', 'loop', 'ambos')),
  constraint ruta_paradas_orden_chk check (orden >= 0)
);

create index if not exists ruta_paradas_ruta_sentido_orden_idx
  on public.ruta_paradas (ruta_id, sentido, orden);

create index if not exists ruta_paradas_parada_idx
  on public.ruta_paradas (parada_id);

create index if not exists ruta_paradas_subida_idx
  on public.ruta_paradas (ruta_id, sentido, orden)
  where es_subida = true;

create index if not exists ruta_paradas_bajada_idx
  on public.ruta_paradas (ruta_id, sentido, orden)
  where es_bajada = true;

create table if not exists public.transbordos (
  id bigint generated by default as identity primary key,
  parada_origen_id bigint not null references public.paradas(id) on delete cascade,
  parada_destino_id bigint not null references public.paradas(id) on delete cascade,
  distancia_caminando_m integer not null,
  tiempo_caminando_min integer generated always as (
    greatest(1, round(distancia_caminando_m / 80.0))
  ) stored,
  activo boolean not null default true,
  bidireccional boolean not null default true,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint transbordos_unique_pair unique (parada_origen_id, parada_destino_id),
  constraint transbordos_distancia_chk check (distancia_caminando_m >= 0),
  constraint transbordos_paradas_distintas_chk check (parada_origen_id <> parada_destino_id)
);

create index if not exists transbordos_origen_activo_idx
  on public.transbordos (parada_origen_id)
  where activo = true;

create index if not exists transbordos_destino_activo_idx
  on public.transbordos (parada_destino_id)
  where activo = true;

