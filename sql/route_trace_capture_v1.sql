on public.route_trace_points
for insert
to authenticated
with check (
  exists (
    select 1
    from public.route_trace_sessions s
    where s.id = trace_session_id
      and s.user_id = auth.uid()
  )
);

drop policy if exists route_trace_points_select_authenticated on public.route_trace_points;
create policy route_trace_points_select_authenticated
on public.route_trace_points
for select
to authenticated
using (true);

