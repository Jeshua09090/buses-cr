create or replace function public.planner_resolve_service_group_candidates(
  p_origin_lat double precision,
  p_origin_lng double precision,
  p_destination_lat double precision,
  p_destination_lng double precision
)
returns table (
  origin_hub_key text,
  origin_hub_name text,
  destination_hub_key text,
  destination_hub_name text,
  group_key text,
  group_name text,
  group_type text,
  route_code text,
  product_route_id integer,
  preview_route_id integer,
  variant_family_code text,
  variant_code text,
  member_label text,
  member_priority integer,
  directness_rank integer,
  effective_priority integer,
  metadata jsonb
)
language sql
stable
set search_path = public, extensions
as $$
with origin_point as (
  select st_setsrid(st_makepoint(p_origin_lng, p_origin_lat), 4326)::geography as geo
),
destination_point as (
  select st_setsrid(st_makepoint(p_destination_lng, p_destination_lat), 4326)::geography as geo
),
origin_hubs as (
  select
    h.id,
    h.hub_key,
    h.hub_name,
    h.priority,
    st_distance(h.geo, op.geo)::integer as distance_m
  from public.planner_hubs h
  cross join origin_point op
  where h.activo = true
    and st_dwithin(h.geo, op.geo, h.radius_m)
),
destination_hubs as (
  select
    h.id,
    h.hub_key,
    h.hub_name,
    h.priority,
    st_distance(h.geo, dp.geo)::integer as distance_m
  from public.planner_hubs h
  cross join destination_point dp
  where h.activo = true
    and st_dwithin(h.geo, dp.geo, h.radius_m)
),
destination_groups as (
  select
    dh.hub_key as destination_hub_key,
    dh.hub_name as destination_hub_name,
    hsg.service_group_id,
    hsg.priority as destination_priority,
    dh.priority as destination_hub_priority,
    dh.distance_m as destination_hub_distance_m
  from destination_hubs dh
  join public.planner_hub_service_groups hsg
    on hsg.hub_id = dh.id
   and hsg.activo = true
   and hsg.role in ('destination', 'both')
),
origin_groups as (
  select
    oh.hub_key as origin_hub_key,
    oh.hub_name as origin_hub_name,
    hsg.service_group_id,
    hsg.priority as origin_priority,
    oh.priority as origin_hub_priority,
    oh.distance_m as origin_hub_distance_m
  from origin_hubs oh
  join public.planner_hub_service_groups hsg
    on hsg.hub_id = oh.id
   and hsg.activo = true
   and hsg.role in ('origin', 'both')
),
origin_scoped_groups as (
  select distinct service_group_id
  from public.planner_hub_service_groups
  where activo = true
    and role in ('origin', 'both')
)
select
  og.origin_hub_key,
  og.origin_hub_name,
  dg.destination_hub_key,
  dg.destination_hub_name,
  sg.group_key,
  sg.group_name,
  sg.group_type,
  gm.route_code,
  gm.product_route_id,
  gm.preview_route_id,
  gm.variant_family_code,
  gm.variant_code,
  gm.member_label,
  gm.member_priority,
  gm.directness_rank,
  (
    sg.priority
    + dg.destination_priority
    + dg.destination_hub_priority
    + least(dg.destination_hub_distance_m, 500) / 25
    + coalesce(og.origin_priority, 0)
    + coalesce(og.origin_hub_priority, 0)
    + coalesce(least(og.origin_hub_distance_m, 500) / 25, 0)
    + gm.member_priority
    + gm.directness_rank
  )::integer as effective_priority,
  jsonb_strip_nulls(
    sg.metadata
    || jsonb_build_object(
      'member_metadata', gm.metadata,
      'origin_required', (osg.service_group_id is not null)
    )
  ) as metadata
from destination_groups dg
join public.planner_service_groups sg
  on sg.id = dg.service_group_id
 and sg.activo = true
join public.planner_service_group_members gm
  on gm.service_group_id = sg.id
 and gm.activo = true
 and gm.service_role in ('destination', 'both')
left join origin_groups og
  on og.service_group_id = sg.id
left join origin_scoped_groups osg
  on osg.service_group_id = sg.id
where osg.service_group_id is null
   or og.service_group_id is not null
order by effective_priority asc, gm.member_priority asc, gm.directness_rank asc, gm.member_label asc;
$$;

