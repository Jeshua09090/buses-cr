drop view if exists public.planner_service_group_member_qa;
create or replace view public.planner_service_group_member_qa as
select
  sg.group_key,
  sg.group_name,
  sg.group_type,
  sg.priority as group_priority,
  gm.member_label,
  gm.route_code,
  gm.product_route_id,
  gm.preview_route_id,
  gm.variant_family_code,
  gm.variant_code,
  gm.member_priority,
  gm.directness_rank,
  gm.activo as member_active,
  ph.hub_key,
  ph.hub_name,
  hsg.role as hub_role,
  hsg.priority as hub_priority,
  r.nombre_ruta as preview_route_name
from public.planner_service_groups sg
join public.planner_service_group_members gm
  on gm.service_group_id = sg.id
left join public.planner_hub_service_groups hsg
  on hsg.service_group_id = sg.id
left join public.planner_hubs ph
  on ph.id = hsg.hub_id
left join public.rutas r
  on r.id = gm.preview_route_id
order by sg.priority asc, sg.group_name asc, gm.member_priority asc, gm.directness_rank asc;

grant select on public.planner_service_group_member_qa to anon, authenticated;

