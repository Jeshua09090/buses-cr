create table if not exists public.planner_service_groups (
  id bigint generated by default as identity primary key,
  group_key text not null unique,
  group_name text not null,
  group_type text not null default 'preferred_alternatives',
  description text,
  priority integer not null default 100,
  activo boolean not null default true,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint planner_service_groups_type_chk
    check (group_type in ('preferred_alternatives', 'equivalent_destination', 'equivalent_origin', 'transfer_bundle')),
  constraint planner_service_groups_priority_chk
    check (priority >= 0)
);

create index if not exists planner_service_groups_lookup_idx
  on public.planner_service_groups (activo, priority, group_type);

create table if not exists public.planner_service_group_members (
  id bigint generated by default as identity primary key,
  service_group_id bigint not null references public.planner_service_groups(id) on delete cascade,
  product_route_id integer,
  preview_route_id integer references public.rutas(id) on delete set null,
  route_code text not null,
  variant_family_code text,
  variant_code text,
  member_label text not null,
  service_role text not null default 'both',
  member_priority integer not null default 100,
  directness_rank integer not null default 100,
  activo boolean not null default true,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint planner_service_group_members_role_chk
    check (service_role in ('origin', 'destination', 'both')),
  constraint planner_service_group_members_priority_chk
    check (member_priority >= 0),
  constraint planner_service_group_members_directness_chk
    check (directness_rank >= 0)
);

create unique index if not exists planner_service_group_members_uidx
  on public.planner_service_group_members (
    service_group_id,
    route_code,
    coalesce(variant_family_code, ''),
    coalesce(variant_code, ''),
    member_label,
    service_role
  );

create index if not exists planner_service_group_members_lookup_idx
  on public.planner_service_group_members (service_group_id, activo, member_priority, directness_rank);

create index if not exists planner_service_group_members_route_idx
  on public.planner_service_group_members (route_code, product_route_id, preview_route_id)
