create table if not exists public.planner_hubs (
  id bigint generated by default as identity primary key,
  hub_key text not null unique,
  hub_name text not null,
  hub_type text not null,
  lat numeric not null,
  lng numeric not null,
  geo geography(Point, 4326) generated always as (
    st_setsrid(st_makepoint((lng)::double precision, (lat)::double precision), 4326)::geography
  ) stored,
  radius_m integer not null default 250,
  planner_radius_override_m integer,
  priority integer not null default 100,
  activo boolean not null default true,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint planner_hubs_type_chk
    check (hub_type in ('terminal', 'center', 'landmark', 'district', 'hospital', 'transfer')),
  constraint planner_hubs_radius_chk
    check (radius_m between 25 and 2000),
  constraint planner_hubs_override_radius_chk
    check (planner_radius_override_m is null or planner_radius_override_m between 50 and 2400)
);

create index if not exists planner_hubs_geo_gix
  on public.planner_hubs using gist (geo);

create index if not exists planner_hubs_lookup_idx
  on public.planner_hubs (activo, priority, hub_type);

create table if not exists public.planner_hub_stops (
  id bigint generated by default as identity primary key,
  hub_id bigint not null references public.planner_hubs(id) on delete cascade,
  parada_id bigint not null references public.paradas(id) on delete cascade,
  role text not null default 'both',
  priority integer not null default 100,
  activo boolean not null default true,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint planner_hub_stops_role_chk
    check (role in ('arrival', 'departure', 'both')),
  constraint planner_hub_stops_priority_chk
    check (priority >= 0),
  constraint planner_hub_stops_hub_stop_role_uidx
    unique (hub_id, parada_id, role)
);

create index if not exists planner_hub_stops_hub_idx
  on public.planner_hub_stops (hub_id, role, activo, priority);

create index if not exists planner_hub_stops_parada_idx
  on public.planner_hub_stops (parada_id, activo);

