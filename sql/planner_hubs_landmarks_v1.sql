create or replace function public.planner_resolve_location_context(
  p_lat double precision,
  p_lng double precision
)
returns table (
  hub_key text,
  hub_name text,
  hub_type text,
  resolved_lat double precision,
  resolved_lng double precision,
  radius_m integer,
  planner_radius_override_m integer,
  arrival_stop_ids bigint[],
  departure_stop_ids bigint[]
)
language sql
stable
set search_path = public, extensions
as $$
with point as (
  select st_setsrid(st_makepoint(p_lng, p_lat), 4326)::geography as g
),
matching_hub as (
  select
    h.id,
    h.hub_key,
    h.hub_name,
    h.hub_type,
    h.lat,
    h.lng,
    h.radius_m,
    h.planner_radius_override_m,
    st_distance(h.geo, p.g)::integer as distance_m
  from point p
  join public.planner_hubs h
    on h.activo = true
   and st_dwithin(h.geo, p.g, h.radius_m)
  order by h.priority asc, distance_m asc, h.id asc
  limit 1
)
select
  h.hub_key,
  h.hub_name,
  h.hub_type,
  h.lat::double precision as resolved_lat,
  h.lng::double precision as resolved_lng,
  h.radius_m,
  h.planner_radius_override_m,
  (
    select coalesce(array_agg(phs.parada_id order by phs.priority asc, phs.parada_id asc), '{}')
    from public.planner_hub_stops phs
    where phs.hub_id = h.id
      and phs.activo = true
      and phs.role in ('arrival', 'both')
  ) as arrival_stop_ids,
  (
    select coalesce(array_agg(phs.parada_id order by phs.priority asc, phs.parada_id asc), '{}')
    from public.planner_hub_stops phs
    where phs.hub_id = h.id
      and phs.activo = true
      and phs.role in ('departure', 'both')
  ) as departure_stop_ids
from matching_hub h;
$$;

grant execute on function public.planner_resolve_location_context(double precision, double precision)
to anon, authenticated;

