create extension if not exists btree_gist with schema extensions;

-- Fase 1 GTFS-like:
-- 1. route_patterns: una variante operativa por ruta y sentido
-- 2. route_pattern_stops: secuencia fija de paradas por variante
-- 3. service_windows: ventanas de servicio basadas en frecuencia por variante
--
-- Esta migracion no elimina ruta_paradas ni ruta_frecuencias.
-- Hace backfill desde ambas para no empezar de cero y permitir una adopcion gradual.

create table if not exists public.route_patterns (
  id bigint generated by default as identity primary key,
  ruta_id bigint not null references public.rutas(id) on delete cascade,
  sentido text not null default 'ida',
  pattern_code text not null,
  nombre text,
  headsign text,
  stop_signature text not null,
  parada_inicial_id bigint references public.paradas(id) on delete set null,
  parada_final_id bigint references public.paradas(id) on delete set null,
  parada_count integer not null default 0,
  distancia_total_m integer,
  activo boolean not null default true,
  fuente text not null default 'legacy_migration',
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint route_patterns_ruta_sentido_code_uidx unique (ruta_id, sentido, pattern_code)
);

do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'route_patterns_sentido_chk'
      and conrelid = 'public.route_patterns'::regclass
  ) then
    alter table public.route_patterns
      add constraint route_patterns_sentido_chk
      check (sentido in ('ida', 'vuelta', 'loop', 'ambos'));
  end if;

  if not exists (
    select 1
    from pg_constraint
    where conname = 'route_patterns_parada_count_chk'
      and conrelid = 'public.route_patterns'::regclass
  ) then
    alter table public.route_patterns
      add constraint route_patterns_parada_count_chk
      check (parada_count >= 0);
  end if;

  if not exists (
    select 1
    from pg_constraint
    where conname = 'route_patterns_fuente_chk'
      and conrelid = 'public.route_patterns'::regclass
  ) then
    alter table public.route_patterns
      add constraint route_patterns_fuente_chk
      check (fuente in ('legacy_migration', 'manual', 'sistema', 'importacion', 'gtfs'));
  end if;
end $$;

create index if not exists route_patterns_ruta_sentido_idx
  on public.route_patterns (ruta_id, sentido)
  where activo = true;

create index if not exists route_patterns_stop_signature_idx
  on public.route_patterns (stop_signature);

create table if not exists public.route_pattern_stops (
  id bigint generated by default as identity primary key,
  pattern_id bigint not null references public.route_patterns(id) on delete cascade,
  parada_id bigint not null references public.paradas(id) on delete cascade,
  stop_sequence integer not null,
  es_subida boolean not null default true,
  es_bajada boolean not null default true,
  pickup_type smallint not null default 0,
  drop_off_type smallint not null default 0,
  distancia_acumulada_m integer,
  tiempo_estimado_desde_inicio_min integer,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint route_pattern_stops_pattern_sequence_uidx unique (pattern_id, stop_sequence)
);

do $$
begin
