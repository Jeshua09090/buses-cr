  if not exists (
    select 1
    from pg_constraint
    where conname = 'route_pattern_stops_pickup_chk'
      and conrelid = 'public.route_pattern_stops'::regclass
  ) then
    alter table public.route_pattern_stops
      add constraint route_pattern_stops_pickup_chk
      check (pickup_type between 0 and 3);
  end if;

  if not exists (
    select 1
    from pg_constraint
    where conname = 'route_pattern_stops_dropoff_chk'
      and conrelid = 'public.route_pattern_stops'::regclass
  ) then
    alter table public.route_pattern_stops
      add constraint route_pattern_stops_dropoff_chk
      check (drop_off_type between 0 and 3);
  end if;
end $$;

create index if not exists route_pattern_stops_pattern_idx
  on public.route_pattern_stops (pattern_id, stop_sequence);

create index if not exists route_pattern_stops_parada_idx
  on public.route_pattern_stops (parada_id);

create table if not exists public.service_windows (
  id bigint generated by default as identity primary key,
  pattern_id bigint not null references public.route_patterns(id) on delete cascade,
  dia_tipo text not null default 'habil',
  hora_inicio time not null,
  hora_fin time not null,
  frecuencia_promedio_min integer not null,
  espera_promedio_min integer generated always as (
    greatest(1, ceil(frecuencia_promedio_min::numeric / 2.0)::integer)
  ) stored,
  minuto_inicio integer generated always as (
    extract(hour from hora_inicio)::integer * 60
    + extract(minute from hora_inicio)::integer
  ) stored,
  minuto_fin integer generated always as (
    extract(hour from hora_fin)::integer * 60
    + extract(minute from hora_fin)::integer
  ) stored,
  activo boolean not null default true,
  source_ruta_frecuencia_id bigint references public.ruta_frecuencias(id) on delete set null,
  notas text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'service_windows_dia_tipo_chk'
      and conrelid = 'public.service_windows'::regclass
  ) then
    alter table public.service_windows
      add constraint service_windows_dia_tipo_chk
      check (dia_tipo in ('habil', 'sabado', 'domingo', 'feriado'));
  end if;

  if not exists (
    select 1
    from pg_constraint
    where conname = 'service_windows_hora_rango_chk'
      and conrelid = 'public.service_windows'::regclass
  ) then
    alter table public.service_windows
      add constraint service_windows_hora_rango_chk
      check (hora_fin > hora_inicio);
  end if;

  if not exists (
    select 1
    from pg_constraint
    where conname = 'service_windows_frecuencia_chk'
      and conrelid = 'public.service_windows'::regclass
  ) then
    alter table public.service_windows
      add constraint service_windows_frecuencia_chk
      check (frecuencia_promedio_min between 1 and 240);
  end if;
end $$;

do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'service_windows_no_overlap_excl'
      and conrelid = 'public.service_windows'::regclass
  ) then
    alter table public.service_windows
      add constraint service_windows_no_overlap_excl
      exclude using gist (
        pattern_id with =,
        dia_tipo with =,
        int4range(minuto_inicio, minuto_fin, '[)') with &&
      )
      where (activo);
  end if;
end $$;

