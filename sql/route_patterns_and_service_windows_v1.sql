comment on table public.route_patterns is
  'Variantes operativas por ruta y sentido. Capa intermedia GTFS-like para separar patrones reales de viaje.';

comment on table public.route_pattern_stops is
  'Secuencia canonica de paradas por route_pattern. Equivale a una version ligera de stop_times sin horarios exactos.';

comment on table public.service_windows is
  'Ventanas de servicio por route_pattern, dia y franja horaria. Equivale a una capa ligera de frequencies/calendar.';

comment on column public.route_patterns.stop_signature is
  'Hash estable de la secuencia de paradas y flags pickup/dropoff. Permite detectar patrones equivalentes.';

comment on column public.service_windows.source_ruta_frecuencia_id is
  'Traza de migracion desde la fila legacy en ruta_frecuencias.';

with legacy_patterns as (
  select
    rp.ruta_id,
    rp.sentido,
    md5(
      string_agg(
        concat_ws(
          ':',
          rp.parada_id::text,
          rp.orden::text,
          case when rp.es_subida then '1' else '0' end,
          case when rp.es_bajada then '1' else '0' end
        ),
        '|'
        order by rp.orden asc
      )
    ) as stop_signature,
    count(*)::integer as parada_count,
    max(rp.distancia_acumulada_m) as distancia_total_m,
    (array_agg(rp.parada_id order by rp.orden asc))[1] as parada_inicial_id,
    (array_agg(rp.parada_id order by rp.orden desc))[1] as parada_final_id,
    (array_agg(coalesce(p.nombre, 'Parada de buses') order by rp.orden asc))[1] as parada_inicial_nombre,
    (array_agg(coalesce(p.nombre, 'Parada de buses') order by rp.orden desc))[1] as parada_final_nombre
  from public.ruta_paradas rp
  join public.paradas p
    on p.id = rp.parada_id
  group by rp.ruta_id, rp.sentido
)
insert into public.route_patterns (
  ruta_id,
  sentido,
  pattern_code,
  nombre,
  headsign,
  stop_signature,
  parada_inicial_id,
  parada_final_id,
  parada_count,
  distancia_total_m,
  activo,
  fuente,
  metadata
)
select
  lp.ruta_id,
  lp.sentido,
  concat('legacy-', lp.sentido, '-', substr(lp.stop_signature, 1, 12)) as pattern_code,
  concat(
    coalesce(r.nombre_ruta, concat('Ruta ', lp.ruta_id::text)),
    ' / ',
    upper(lp.sentido)
  ) as nombre,
  lp.parada_final_nombre as headsign,
  lp.stop_signature,
  lp.parada_inicial_id,
  lp.parada_final_id,
  lp.parada_count,
  lp.distancia_total_m,
  true,
  'legacy_migration',
  jsonb_build_object(
    'migrated_from', 'ruta_paradas',
    'legacy_ruta_id', lp.ruta_id,
    'legacy_sentido', lp.sentido
  )
from legacy_patterns lp
left join public.rutas r
  on r.id = lp.ruta_id
on conflict (ruta_id, sentido, pattern_code)
do update set
  nombre = excluded.nombre,
  headsign = excluded.headsign,
  stop_signature = excluded.stop_signature,
  parada_inicial_id = excluded.parada_inicial_id,
  parada_final_id = excluded.parada_final_id,
  parada_count = excluded.parada_count,
  distancia_total_m = excluded.distancia_total_m,
  activo = true,
  metadata = public.route_patterns.metadata || excluded.metadata,
  updated_at = timezone('utc', now());

with legacy_patterns as (
  select
    rp.ruta_id,
    rp.sentido,
    md5(
      string_agg(
        concat_ws(
          ':',
          rp.parada_id::text,
          rp.orden::text,
          case when rp.es_subida then '1' else '0' end,
          case when rp.es_bajada then '1' else '0' end
        ),
        '|'
        order by rp.orden asc
      )
    ) as stop_signature
  from public.ruta_paradas rp
  group by rp.ruta_id, rp.sentido
),
resolved_patterns as (
  select
    lp.ruta_id,
    lp.sentido,
    rp.id as pattern_id
  from legacy_patterns lp
  join public.route_patterns rp
    on rp.ruta_id = lp.ruta_id
   and rp.sentido = lp.sentido
   and rp.stop_signature = lp.stop_signature
   and rp.fuente = 'legacy_migration'
)
insert into public.route_pattern_stops (
  pattern_id,
  parada_id,
  stop_sequence,
  es_subida,
  es_bajada,
  pickup_type,
  drop_off_type,
  distancia_acumulada_m,
  tiempo_estimado_desde_inicio_min
)
select
  resolved.pattern_id,
  rp.parada_id,
  rp.orden as stop_sequence,
  rp.es_subida,
  rp.es_bajada,
  case when rp.es_subida then 0 else 1 end as pickup_type,
  case when rp.es_bajada then 0 else 1 end as drop_off_type,
  rp.distancia_acumulada_m,
  rp.tiempo_estimado_desde_inicio_min
from public.ruta_paradas rp
join resolved_patterns resolved
  on resolved.ruta_id = rp.ruta_id
 and resolved.sentido = rp.sentido
on conflict (pattern_id, stop_sequence)
do update set
  parada_id = excluded.parada_id,
  es_subida = excluded.es_subida,
  es_bajada = excluded.es_bajada,
  pickup_type = excluded.pickup_type,
  drop_off_type = excluded.drop_off_type,
  distancia_acumulada_m = excluded.distancia_acumulada_m,
  tiempo_estimado_desde_inicio_min = excluded.tiempo_estimado_desde_inicio_min,
  updated_at = timezone('utc', now());

insert into public.service_windows (
  pattern_id,
  dia_tipo,
  hora_inicio,
  hora_fin,
  frecuencia_promedio_min,
  activo,
  source_ruta_frecuencia_id,
  notas,
  metadata
)
select
  rp.id as pattern_id,
  rf.dia_tipo,
  rf.hora_inicio,
  rf.hora_fin,
  rf.frecuencia_promedio_min,
  rf.activo,
  rf.id as source_ruta_frecuencia_id,
  rf.notas,
  jsonb_build_object(
    'migrated_from', 'ruta_frecuencias',
    'legacy_ruta_id', rf.ruta_id,
    'legacy_sentido', rf.sentido
  )
from public.ruta_frecuencias rf
join public.route_patterns rp
  on rp.ruta_id = rf.ruta_id
 and rp.sentido = rf.sentido
 and rp.fuente = 'legacy_migration'
where not exists (
  select 1
  from public.service_windows sw
  where sw.pattern_id = rp.id
    and sw.source_ruta_frecuencia_id = rf.id
);
